create table user
(
    id          int auto_increment
        primary key,
    updated_by  int                                                     null,
    avatar      varchar(500)                                            null,
    created_at  datetime(6)                                             null,
    created_by  int                                                     null,
    email       varchar(100)                                            not null,
    full_name   varchar(255)                                            not null,
    is_enable   tinyint(1) default 0                                    not null,
    is_verified tinyint(1) default 0                                    not null,
    note        varchar(255)                                            null,
    password    varchar(255)                                            not null,
    phone       varchar(10)                                             not null,
    role        enum ('ADMIN', 'LECTURE', 'STUDENT', 'SUBJECT_MANAGER') not null,
    token       varchar(255)                                            null,
    updated_at  datetime(6)                                             null,
    constraint UK_ob8kqyqqgmefl0aco34akdtpe
        unique (email)
);

create table subject
(
    id         int auto_increment
        primary key,
    code       varchar(6)           not null,
    created_at datetime(6)          null,
    is_enable  tinyint(1) default 1 not null,
    name       varchar(50)          not null,
    updated_at datetime(6)          null,
    updated_by int                  null,
    creator_id int                  not null,
    manager_id int                  not null,
    constraint UK_db5g1rfeug7aywnpb6gab85ep
        unique (code),
    constraint FKmnsjcss4kf1bjhcqd6wwoem3r
        foreign key (creator_id) references user (id),
    constraint FKnymjcv6wa4jysqsm7sw9trxiv
        foreign key (manager_id) references user (id)
);

create table assignment
(
    id          int auto_increment
        primary key,
    updated_by  int          null,
    created_at  datetime(6)  null,
    description varchar(255) null,
    title       varchar(100) not null,
    updated_at  datetime(6)  null,
    creator_id  int          not null,
    subject_id  int          not null,
    constraint FK80ahho21takmnctnwwd99uyes
        foreign key (creator_id) references user (id),
    constraint FKp1gsvxfhi4e5ek8pubhmnh6ol
        foreign key (subject_id) references subject (id)
);

create table class
(
    id         int auto_increment
        primary key,
    updated_by int          null,
    code       varchar(6)   not null,
    created_at datetime(6)  null,
    detail     varchar(255) null,
    updated_at datetime(6)  null,
    creator_id int          not null,
    manager_id int          not null,
    subject_id int          not null,
    status     tinyint      not null,
    constraint UK_mtq71nmcpoqpxqvgklo7v06na
        unique (code),
    constraint FKe8femv4qxrc56ehlcuy5pdmew
        foreign key (manager_id) references user (id),
    constraint FKfvehqkaor8vakf9l4ed9p9c9r
        foreign key (creator_id) references user (id),
    constraint FKh0eyrgvqpfux7dvr8elhhdaf6
        foreign key (subject_id) references subject (id),
    check (`status` between 0 and 3)
);

create table class_student
(
    class_id   int not null,
    student_id int not null,
    primary key (class_id, student_id),
    constraint FK7sbm5g487ge9v25pyh8tvs8lg
        foreign key (student_id) references user (id),
    constraint FKgt5cku1apa8h7jdbr2np7emg8
        foreign key (class_id) references class (id)
